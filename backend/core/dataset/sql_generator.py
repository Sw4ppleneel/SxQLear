from __future__ import annotations

import textwrap
from datetime import datetime

from models.dataset import DatasetPlan, JoinType


class SQLGenerator:
    """
    Generates annotated SQL from a DatasetPlan.

    This is the final step in the dataset construction pipeline.
    The SQL is a derived output — the DatasetPlan is the primary artifact.

    Output characteristics:
    - Well-formatted, readable SQL (not minified)
    - Inline comments that reference the plan's reasoning
    - Explicit column aliasing for clarity
    - Warnings included as SQL comments at the top
    - Never modifies the original DatasetPlan
    """

    def generate(self, plan: DatasetPlan) -> str:
        """Generate annotated SQL for the given DatasetPlan."""
        parts: list[str] = []

        # ── File header comment ───────────────────────────────────────────────
        parts.append(self._build_header(plan))

        # ── SELECT ────────────────────────────────────────────────────────────
        parts.append(self._build_select(plan))

        # ── FROM ──────────────────────────────────────────────────────────────
        parts.append(f"FROM {plan.base_table}")

        # ── JOINs ─────────────────────────────────────────────────────────────
        for join in plan.joins:
            parts.append(self._build_join(join))

        # ── WHERE ─────────────────────────────────────────────────────────────
        if plan.filters:
            parts.append(self._build_where(plan))

        sql = "\n".join(parts) + ";"
        return sql

    @staticmethod
    def _build_header(plan: DatasetPlan) -> str:
        lines = [
            "-- ═══════════════════════════════════════════════════════════",
            f"-- Dataset: {plan.name}",
            f"-- {plan.description}",
            f"-- Generated by SxQLear on {datetime.utcnow().strftime('%Y-%m-%d %H:%M UTC')}",
            f"-- Plan ID: {plan.id}",
            "--",
        ]

        if plan.grain_description:
            lines.append(f"-- GRAIN: {plan.grain_description}")
            lines.append("--")

        if plan.assumptions:
            lines.append("-- ASSUMPTIONS:")
            for assumption in plan.assumptions:
                wrapped = textwrap.wrap(assumption, width=70)
                for i, line in enumerate(wrapped):
                    prefix = "--   " if i > 0 else "--   • "
                    lines.append(f"{prefix}{line}")
            lines.append("--")

        if plan.warnings:
            lines.append("-- ⚠ WARNINGS — Review before using in production:")
            for warning in plan.warnings:
                wrapped = textwrap.wrap(warning, width=70)
                for i, line in enumerate(wrapped):
                    prefix = "--   " if i > 0 else "--   ⚠ "
                    lines.append(f"{prefix}{line}")
            lines.append("--")

        lines.append("-- ═══════════════════════════════════════════════════════════")
        return "\n".join(lines)

    @staticmethod
    def _build_select(plan: DatasetPlan) -> str:
        if not plan.selected_columns:
            return "SELECT *"

        col_parts: list[str] = []
        for sel in plan.selected_columns:
            expr = sel.transformation or f'"{sel.table}"."{sel.column}"'
            if sel.alias:
                expr = f"{expr} AS {sel.alias}"
            if sel.notes:
                col_parts.append(f"  {expr}  -- {sel.notes}")
            else:
                col_parts.append(f"  {expr}")

        return "SELECT\n" + ",\n".join(col_parts)

    @staticmethod
    def _build_join(join) -> str:
        join_type = join.join_type.value if isinstance(join.join_type, JoinType) else join.join_type
        clause = (
            f'{join_type} JOIN "{join.right_table}"\n'
            f'  ON "{join.left_table}"."{join.left_column}" = '
            f'"{join.right_table}"."{join.right_column}"'
        )
        if join.reasoning:
            clause += f"\n  -- {join.reasoning}"
        return clause

    @staticmethod
    def _build_where(plan: DatasetPlan) -> str:
        conditions: list[str] = []
        for i, f in enumerate(plan.filters):
            prefix = "WHERE" if i == 0 else "  AND"
            val = f'"{f.value}"' if isinstance(f.value, str) else str(f.value)
            conditions.append(f'{prefix} "{f.table}"."{f.column}" {f.operator} {val}')
            if f.reasoning:
                conditions[-1] += f"  -- {f.reasoning}"
        return "\n".join(conditions)
